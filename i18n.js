function changeLanguage(langCode) {
    document.documentElement.lang = langCode;
    localStorage.setItem('preferredLanguage', langCode);
    
    // Update language labels (DE, EN, etc)
    const langMap = { 'de': 'DE', 'en': 'EN', 'fr': 'FR', 'ru': 'RU' };
    document.querySelectorAll('.current-lang-label').forEach(el => {
        if (el) el.innerText = langMap[langCode];
    });

    // Translate DOM elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[key] && translations[key][langCode]) {
            el.innerHTML = translations[key][langCode];
        }
    });
    
    // Update mobile sidebar language buttons
    const langBtns = ['de', 'en', 'fr', 'ru'];
    langBtns.forEach(lang => {
        const btn = document.getElementById(`lang-btn-${lang}`);
        if (btn) {
            if (lang === langCode) {
                // Active styles
                btn.className = "text-left px-4 py-3 rounded-lg bg-neutral-50 text-sm font-medium text-neutral-900 border border-neutral-200 hover:bg-neutral-100";
            } else {
                // Inactive styles
                btn.className = "text-left px-4 py-3 rounded-lg text-sm font-medium text-neutral-600 hover:bg-neutral-50 border border-transparent";
            }
        }
    });

    // Dispatch event so React Wizard can pick up the change
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: langCode } }));
}

// Auto-load preferred language on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'de';
    if (savedLang !== 'de') {
        changeLanguage(savedLang);
    }
});
